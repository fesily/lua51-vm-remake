function testfn(f)
	print(f(12))
	print(f(12, 34))
	print(f(34, 12))
	print(f(0/0.0, 1234))
	print(f(1234, 0/0.0))
	print(f(34, 12, 56))
	print(f(56, 12, 34))
	print(f(12, 56, 34))
	print(f(1.0/0, 12, 56))
	print(f(-1.0/0, 12, 56))
	print(f(12, 56, 1.0/0))
	print(f(12, 56, -1.0/0))
	print(f(1,2,3,4,5,6,7))
	print(f(7,6,5,4,3,2,1))
	print(f(4,3,2,1,7,6,5))
	print(f("0xaa", "0xbb"))
	print(f("0xbb", "0xaa"))
	print((pcall(function() f() end)))
	print((pcall(function() f("a") end)))
	print((pcall(function() f(12, "b") end)))
end

print('-- max --')
testfn(math.max)

print('-- min --')
testfn(math.min)

